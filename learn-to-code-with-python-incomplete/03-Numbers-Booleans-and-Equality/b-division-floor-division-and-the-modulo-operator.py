# division : the type of result always float number
print(15 / 3)
print(14 / 3)

# floor division : chop off the decimail component of the result. The result type is Integer
print(14 // 3)

# negative number floor division: chop off the decimal component and then
# round down away from 0 to the nearest integer. 
print(-14 // 3)


# modulor / remainder 
print(14 % 3) 
print(15 % 3) 
print(16 % 3)
print(1 % 3)
print(2 % 3)