# oop : views a program as a collection of objects that interact with each other
# Object: is a container of data and behavior
# attributes: define an object's state or internal data. 
# Method: a method can be viewed as a function belonging to an object. 
# method is a command or an instruction or a message to send to an object. 
# method can interact with and modify the object's attributes, thus altering its state.

# classes: a blueprint for defining a new object type in Python. 
# a class definition describes the attributes and methods that each object made from the class will have.

# instances: an object created from from a class. the act of creating an intance is called instantiation. 
# Objects created from the same class are independent of each other.