"""
3 built-in functions that help us convert date from one form to another
"""

# int() : converts its argument to an integer /to a whole number. 
# argument can be integer, string (integer value), float number
# chop off the decimal point, not rounding
print(int(3.14))  
print(int(3.99))
print(int("3"))
print(int(3))

# float(): converts its argument to an float number
# argument can be integer, string(integer/ float value), float number
print(float(3))
print(float("3"))  # no error: 3.0 
print(float("3.45")) 

# str()
print(str(25.3) + "hello") # 25.3hello
print(str(5) * 3) # 555




