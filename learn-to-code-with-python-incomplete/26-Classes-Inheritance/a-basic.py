# inheritance is a design pattern in whihc a class inherits attributes and methods from one or more other classes
# helps to organize related classes and reduce duplication

# the class being inherited from is called the parent, superclass, or base class
# the call that inherits is called the child, subclass, or derived class

# public and protected attributes beginning with a single underscore are inherited by the subclass, as are _methods
# private, name-mangled attributes beginning with __ are not inherited by the subclass

# type-of relationships
# A Coffeehsop is a type of Store
# A Horse is a type of Animal
# A Train is a type of Vehicle

